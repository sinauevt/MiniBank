package Coba.Report;

public class CekSaldo {
	String NIA;
	String Password;
	String Saldo;
	public String getNIA() {
		return NIA;
	}
	public void setNIA(String nIA) {
		NIA = nIA;
	}
	public String getPassword() {
		return Password;
	}
	public void setPassword(String password) {
		Password = password;
	}
	public String getSaldo() {
		return Saldo;
	}
	public void setSaldo(String saldo) {
		Saldo = saldo;
	}
}
