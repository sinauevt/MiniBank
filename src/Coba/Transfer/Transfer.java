package Coba.Transfer;

public class Transfer {
	String NOTS;
	String Tanggal;
	String NIA1;
	String NIA2;
	String Uang;

	public String getNOTS() {
		return NOTS;
	}

	public void setNOTS(String nOTS) {
		NOTS = nOTS;
	}

	public String getTanggal() {
		return Tanggal;
	}

	public void setTanggal(String tanggal) {
		Tanggal = tanggal;
	}

	public String getNIA1() {
		return NIA1;
	}

	public void setNIA1(String nIA1) {
		NIA1 = nIA1;
	}

	public String getNIA2() {
		return NIA2;
	}

	public void setNIA2(String nIA2) {
		NIA2 = nIA2;
	}

	public String getUang() {
		return Uang;
	}

	public void setUang(String uang) {
		Uang = uang;
	}

}
