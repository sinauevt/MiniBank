package Coba.TramsaksiAMB;

public class TransaksiAmbil {

	String NoTS;
	String NIA;
	String Nama;
	String Alamat;
	String Tanggal;
	String Saldo;
	String Uang;
	String TotalSaldo;

	public String getNoTS() {
		return NoTS;
	}

	public void setNoTS(String noTS) {
		NoTS = noTS;
	}

	public String getNIA() {
		return NIA;
	}

	public void setNIA(String nIA) {
		NIA = nIA;
	}

	public String getNama() {
		return Nama;
	}

	public void setNama(String nama) {
		Nama = nama;
	}

	public String getAlamat() {
		return Alamat;
	}

	public void setAlamat(String alamat) {
		Alamat = alamat;
	}

	public String getTanggal() {
		return Tanggal;
	}

	public void setTanggal(String tanggal) {
		Tanggal = tanggal;
	}

	public String getSaldo() {
		return Saldo;
	}

	public void setSaldo(String saldo) {
		Saldo = saldo;
	}

	public String getUang() {
		return Uang;
	}

	public void setUang(String uang) {
		Uang = uang;
	}

	public String getTotalSaldo() {
		return TotalSaldo;
	}

	public void setTotalSaldo(String totalSaldo) {
		TotalSaldo = totalSaldo;
	}
}
